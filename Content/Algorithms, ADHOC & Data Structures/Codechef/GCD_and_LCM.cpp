ll power(ll x, ll p)
{
	if (p == 0) return (1);
	ll res = power(x, p/2);
	if (p%2)
		return ((res * res) * x);
	return (res * res);
}
ll gcd(ll a, ll b)
{
	if (b == 0) return (a);
	return (gcd(b, a%b));
}
ll lcm(ll a, ll b)
{
	return ((a*b)/gcd(a, b));
}

void solve()
{
	ll l, g, x, y, k;

	cin >> x >> y >> k;

	for (ll i = 0; i < k; i++)
	{
		g = gcd(x, y);

		if (x < y)
			y = g;
		else
			x = g;

		l = lcm(x, y);

		if (x < y)
			y = l;
		else
			x = l;

		if (x == y) break;
	}

	cout << x + y << "\n";
}

int main()
{
	ios::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);
	ll t; cin >> t;
	while (t--)
		solve();
}
